package com.edgenius.wiki.ext.calendar;

import java.io.Serializable;

/**
 * A json special for xgCalendar input format 
 * @author Dapeng.Ni
 */
public class CalendarErrorJson implements Serializable{
	private static final long serialVersionUID = 5378427753337073730L;
	
	private String errorCode;
	private String errorMsg;
	/**
	 */
	public CalendarErrorJson(String errorCode, String errorMsg) {
		this.errorCode = errorCode;
		this.errorMsg = errorMsg;
	}
	/**
	 * @return the errorCode
	 */
	public String getErrorCode() {
		return errorCode;
	}
	/**
	 * @param errorCode the errorCode to set
	 */
	public void setErrorCode(String errorCode) {
		this.errorCode = errorCode;
	}
	/**
	 * @return the errorMsg
	 */
	public String getErrorMsg() {
		return errorMsg;
	}
	/**
	 * @param errorMsg the errorMsg to set
	 */
	public void setErrorMsg(String errorMsg) {
		this.errorMsg = errorMsg;
	}

}
